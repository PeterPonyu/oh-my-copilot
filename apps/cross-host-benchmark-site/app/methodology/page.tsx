import { buildSitePresentation } from '../../src/lib/generated'

const formatTimestamp = (timestamp: string): string =>
  new Intl.DateTimeFormat('en-US', {
    dateStyle: 'medium',
    timeStyle: 'short',
    timeZone: 'UTC',
  }).format(new Date(timestamp))

export default function MethodologyPage() {
  const { manifest } = buildSitePresentation()

  return (
    <section className="panel-stack flagship-stack">
      <section className="panel hero-panel">
        <p className="eyebrow">methodology</p>
        <h2>Comparability classes stay inside the proof contract.</h2>
        <p>
          The shared site reports side-by-side rows only when the generated manifest says how
          comparable they are. Current Copilot/Cursor benchmark rows stay observed and default to{' '}
          <strong>{manifest.comparisonPolicy.currentPairingClass}</strong>.
        </p>
        <p>
          That means the site keeps the sibling comparison visible without pretending the repos run
          one shared harness or expose mechanism-equivalent totals.
        </p>
      </section>

      <section className="metric-grid">
        <article className="metric-card">
          <p className="metric-label">Manifest generated</p>
          <p className="metric-value">{formatTimestamp(manifest.generatedAt)}</p>
          <p className="metric-detail">Generated by {manifest.generator.script}.</p>
        </article>
        <article className="metric-card">
          <p className="metric-label">Allowed classes</p>
          <p className="metric-value">{manifest.comparisonPolicy.allowedComparabilityClasses.length}</p>
          <p className="metric-detail">Outcome-comparable, reporting-comparable, not-comparable.</p>
        </article>
        <article className="metric-card">
          <p className="metric-label">Default pairing</p>
          <p className="metric-value">{manifest.comparisonPolicy.defaultComparabilityClass}</p>
          <p className="metric-detail">Used until row-level proof supports a stronger claim.</p>
        </article>
      </section>

      <section className="panel">
        <h3>Allowed comparability classes</h3>
        <p>
          The canonical set is <strong>outcome-comparable</strong>,{' '}
          <strong>reporting-comparable</strong>, and <strong>not-comparable</strong>. The current
          Copilot/Cursor pairing stays reporting-comparable until row-level proof supports anything
          stronger.
        </p>
        <ul className="check-list">
          {manifest.comparisonPolicy.allowedComparabilityClasses.map((value) => (
            <li key={value}>{value}</li>
          ))}
        </ul>
      </section>

      <section className="card-grid">
        {manifest.repos.map((repo) => (
          <article key={repo.repo} className="panel compare-card">
            <p className="eyebrow">{repo.repo}</p>
            <h3>{repo.comparabilityClass}</h3>
            <p>{repo.comparabilityNote}</p>
            <ul className="check-list proof-list">
              <li>
                <strong>Source branch/SHA</strong> — {repo.sourceBranch}@{repo.sourceSha}
              </li>
              <li>
                <strong>Workspace branch/SHA</strong> — {repo.workspaceBranch}@{repo.workspaceSha}
              </li>
              <li>
                <strong>Snapshot proof</strong> — <span className="proof-link">{repo.snapshotPath}</span>
              </li>
              <li>
                <strong>History source</strong> — <span className="proof-link">{repo.historyPath}</span>
              </li>
              <li>
                <strong>Harvested at</strong> — {formatTimestamp(repo.harvestedAt)}
              </li>
            </ul>
          </article>
        ))}
      </section>
    </section>
  )
}
